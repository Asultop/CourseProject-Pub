/**
 * 主函数：支持两种模式
 * 1. serialize：序列化文件夹并输出C数组
 * 2. deserialize：从嵌入的数组还原文件夹
 */
int main(int argc, char* argv[]) {
    if (argc < 3) {
        fprintf(stderr, "用法：\n");
        fprintf(stderr, "  序列化：%s serialize <源文件夹> <数组变量名>\n", argv[0]);
        fprintf(stderr, "  还原：%s deserialize <输出文件夹>\n", argv[0]);
        return -1;
    }

    // 模式1：序列化文件夹
    if (strcmp(argv[1], "serialize") == 0) {
        if (argc != 4) {
            fprintf(stderr, "序列化用法：%s serialize <源文件夹> <数组变量名>\n", argv[0]);
            return -1;
        }
        const char* srcDir = argv[2];
        const char* varName = argv[3];

        uint8_t* buffer = NULL;
        size_t size = 0;
        if (serialize_folder(srcDir, &buffer, &size) == 0) {
            print_c_array(buffer, size, varName);
            free(buffer);
            printf("序列化完成！请将上方数组复制到代码中替换 embedded_folder_data\n");
        } else {
            return -1;
        }
    }
    // 模式2：还原文件夹
    else if (strcmp(argv[1], "deserialize") == 0) {
        const char* outputDir = argv[2];
        
        // 优先校验嵌入式数据，避免进入解压逻辑触发段错误
        if (embedded_folder_data_size == 0) {
            fprintf(stderr, "错误：未嵌入序列化数据！请先执行 serialize 模式生成数组并替换\n");
            return -1;
        }
        
        // 传入数据前再次校验
        if (embedded_folder_data == NULL) {
            fprintf(stderr, "错误：嵌入式数据指针为空\n");
            return -1;
        }
        
        int ret = deserialize_folder(embedded_folder_data, embedded_folder_data_size, outputDir);
        if (ret != 0) {
            fprintf(stderr, "还原文件夹失败！\n");
            return -1;
        }
    }
    else {
        fprintf(stderr, "无效模式！支持：serialize / deserialize\n");
        return -1;
    }

    return 0;
}